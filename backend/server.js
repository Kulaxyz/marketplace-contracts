const marketplaceContract = new ethers.Contract(marketplaceAddress, marketplaceAbi, provider);
const marketplaceContractWithSigner = marketplaceContract.connect(wallet);

app.get('/listings', (req, res) => {
    connection.query('SELECT * FROM `active_listings`', function (error, results, fields) {
        if (error) throw error;
        res.setHeader('Access-Control-Allow-Origin', '*')
        res.send(results)
    })
})

app.post('/mint-list', async (req, res) => {
    let form = new multiparty.Form();
    form.parse(req, async function(err, fields, files) {
        const { name, description, price } = fields;
        const { responses, respFiles } = await storeImages(files)
        const tokenUri = await getUris(responses, respFiles, name, description)

        const tx = await nftContractWithSigner.mint(tokenUri)
        const receipt = await tx.wait(1)
        const tokenId = receipt.events[0].args.tokenId

        const approvalTx = await nftContractWithSigner.approve(marketplaceAddress, tokenId)
        await approvalTx.wait(1)

        const listingTx = await marketplaceContractWithSigner.listNft(
            nftContractAddress,
            tokenId,
            ethers.utils.parseUnits(price.toString(), 'wei')
        )
        await listingTx.wait(1)
        console.log('Minted and listed NFT!!!')
    });

    res.send('success')
})

